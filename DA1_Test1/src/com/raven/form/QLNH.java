    public void loadTableNhap() {
        String coulum[] = {"Mã Nhập", "Ngày Nhập", "Nhân Viên", "Nhà Cung Cấp", "Tổng Tiền", "Số Lượng", "Hình Thức TT"};
        DefaultTableModel model = new DefaultTableModel(coulum, 0);
        list = NhapHangDao.getAllNhapHang();
        for (NhapHang sv : list) {
            String tongTien = decimalFormat.format(sv.getTongTien());
            model.addRow(new Object[]{
                sv.getMaNhap(),
                sv.getNgayNhap(),
                sv.getMaNV(),
                sv.getMaNCC(),
                tongTien,
                sv.getTongSoLuong(),
                sv.getHinhThucTT()

            });
        }
        tblDanhSachNhapHang.setModel(model);
    }

