    public void loadComboBoxNhaCungCap() {
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        model.removeAllElements();
        listNCC = NhaCungCapDao.getAllNhaCungCap();
        for (NhaCungCap sach : listNCC) {
            model.addElement(sach.getTenNCC());
        }
        cboNhaCC.setModel(model);
    }

    public NhapHang formToNH() {
        NhapHang sv = new NhapHang();
        sv.setMaNhap(txtMaNhap.getText());
        sv.setNgayNhap(txtNgayNhap.getText());
        sv.setTongTien(Float.parseFloat(txtTongTien.getText()));
        sv.setTongSoLuong(Integer.parseInt(txtTongSoLuong.getText()));
        sv.setMaNV(txtMaNV.getText());
        if (cboHTTT.getSelectedItem().equals("Tien Mat")) {
            sv.setHinhThucTT("Tien Mat");
        } else {
            sv.setHinhThucTT("Chuyen Khoan");
        }
        listNCC = NhaCungCapDao.getAllNhaCungCap();
        sv.setMaNCC(listNCC.get(cboNhaCC.getSelectedIndex()).getMaNCC());

        return sv;
    }

    public void fillToForm(int index) {
        NhapHang sv = list.get(index);

        txtMaNhap.setText(sv.getMaNhap());
        txtNgayNhap.setText(sv.getNgayNhap());
        txtMaNV.setText(sv.getMaNV());
        txtTongTien.setText(""+sv.getTongTien());
        cboNhaCC.setSelectedItem(sv.getMaNCC());
        txtTongSoLuong.setText("" + sv.getTongSoLuong());
        cboHTTT.setSelectedItem(sv.getHinhThucTT());
    }

    public void insertNhapHang() {
        NhapHang sv = formToNH();
        int rs = NhapHangDao.insertNhapHang(sv);
        if (rs > 0) {
            JOptionPane.showMessageDialog(this, "Thêm Thành Công");
            loadTableNhap();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm Thất Bại");
        }
    }

    public void updateNhapHang() {
        NhapHang sv = formToNH();
        int rs = NhapHangDao.updateNhapHang(sv);
        if (rs > 0) {
            JOptionPane.showMessageDialog(this, "Cập Nhật Thành Công");
            loadTableNhap();
        } else {
            JOptionPane.showMessageDialog(this, "Cập Nhật Thất Bại");
        }
    }

    public void deleteNhapHang() {
        int ma = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa Hóa Đơn Nhập Hàng này không?");
        if (ma == 0) {
            int rs = NhapHangDao.deleteNhapHang(txtMaNhap.getText());
            if (rs > 0) {
                JOptionPane.showMessageDialog(this, "Xoá Thành Công");
                loadTableNhap();
            } else {
                JOptionPane.showMessageDialog(this, "xóa Thất Bại");
            }
        } else {
            return;
        }
    }

    public boolean check() {
        if (txtMaNhap.getText().isBlank()) {
            jlbMaNhap.setText("Không Được Để Trống Mã Nhập!!");
            jlbMaNhap.setForeground(Color.red);
            return false;
        } else {
            jlbMaNhap.setForeground(new Color(0, 0, 0, 0));
        }
        return true;
    }

    public boolean checkMaTT() {
        list = NhapHangDao.getAllNhapHang();
        for (NhapHang nhapHang : list) {
            if (txtMaNhap.getText().equals(nhapHang.getMaNhap())) {
                jlbMaNhap.setText("Mã Nhập Đã Tồn Tại!!");
                jlbMaNhap.setForeground(Color.red);
                return false;
            } else {
                jlbMaNhap.setForeground(new Color(0, 0, 0, 0));
            }
        }
        return true;
    }

    public boolean checkMaKoTT() {
        list = NhapHangDao.getAllNhapHang();
        for (NhapHang nhapHang : list) {
            if (txtMaNhap.getText().equals(nhapHang.getMaNhap())) {
                jlbMaNhap.setForeground(new Color(0, 0, 0, 0));
            }
            return true;
        }
        jlbMaNhap.setText("Mã Nhập Không Tồn Tại!!");
        jlbMaNhap.setForeground(Color.red);
        return false;
    }
