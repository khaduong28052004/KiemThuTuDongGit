// Màu
    public void loadTableMau() {
        String coulum[] = {"Mã Màu", "Mau"};
        DefaultTableModel model = new DefaultTableModel(coulum, 0);
        listMau = MauDao.getAllMau();
        for (Mau sv : listMau) {
            model.addRow(new Object[]{
                sv.getMaMau(),
                sv.getTenMau()
            });
        }
        tblDanhSachMau.setModel(model);
    }

    public Mau formToMau() {
        Mau sv = new Mau();
        sv.setMaMau(Integer.parseInt(txtMaMau.getText()));
        sv.setTenMau(txtTenMau.getText());
        return sv;
    }

    public void fillToFormMau(int index) {
        Mau sv = listMau.get(index);
        txtMaMau.setText("" + sv.getMaMau());
        txtTenMau.setText(sv.getTenMau());

    }

    public void loadComboBoxMau() {
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        model.removeAllElements();
        listMau = MauDao.getAllMau();
        for (Mau sach : listMau) {
            model.addElement(sach.getTenMau());
        }
        cboMau.setModel(model);
    }

    public void insertMau() {
        Mau sv = formToMau();
        int rs = MauDao.insertMau(sv);
        if (rs > 0) {
            JOptionPane.showMessageDialog(this, "Thêm Thành Công");
            loadComboBoxMau();
            loadTableMau();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm Thất Bại");
        }
    }

    public boolean checkMau() {
        if (txtTenMau.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Không được để trống tên Màu!!");
            return false;
        }
        listMau = MauDao.getAllMau();
        for (Mau mau : listMau) {
            if (txtTenMau.getText().equalsIgnoreCase(mau.getTenMau())) {
                JOptionPane.showMessageDialog(this, "Màu đã tồn tại!!");
                return false;
            }
        }
        return true;
    }

