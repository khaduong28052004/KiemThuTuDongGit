    public void deleteSanPham() {
        int ma = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa Sản Phẩm này không?");
        if (ma == 0) {
            int rs = SanPhamChiTietDao.deleteCTSP(Integer.parseInt(txtMaSPCT.getText()));
            if (rs > 0) {
                JOptionPane.showMessageDialog(this, "Xoá Thành Công");
                loadTablChiTietSP();
            } else {
                JOptionPane.showMessageDialog(this, "xóa Thất Bại");
            }
        } else {
            return;
        }
    }

    public boolean checkSPCT() {
        if (txtSoLuong.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Không được để trông!!");
            return false;
        }
        try {
            Integer sl = Integer.parseInt(txtSoLuong.getText());
            if (sl < 0) {
                JOptionPane.showMessageDialog(this, "Không được nhập số âm!!");
                return false;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số!!");
            return false;
        }
        return true;
    }

